def _infer_style_category(style: PromptStyle) -> str:
    """Infer a main category from explicit key mapping or style tags."""
    key = (style.get("key") or "").strip()
    explicit = _STYLE_CATEGORY_BY_KEY.get(key)
    if explicit:
        return explicit

    tags = {tag.strip().lower() for tag in style.get("tags", [])}
    for tag, category in _STYLE_CATEGORY_HINTS:
        if tag in tags:
            return category

    return _STYLE_CATEGORY_FALLBACK


def _style_label_sort_key(label: str) -> str:
    """Strip bracket tags from labels so sorting is stable and human-readable."""
    return label.split("[", 1)[0].strip().casefold()


def _categorize_and_sort_styles(styles: list[PromptStyle]) -> list[PromptStyle]:
    """Annotate each style with category metadata and sort by category then style."""
    categorized: list[PromptStyle] = []
    seen_keys: set[str] = set()
    for style in styles:
        key = (style.get("key") or "").strip()
        if key:
            if key in seen_keys:
                continue
            seen_keys.add(key)

        raw_label = (style.get("raw_label") or style["label"]).strip()
        category = _infer_style_category(style)
        display_label = f"{category} :: {raw_label}"
        categorized.append(
            cast(
                PromptStyle,
                {
                    **style,
                    "raw_label": raw_label,
                    "main_category": category,
                    "label": display_label,
                },
            )
        )

    categorized.sort(
        key=lambda style: (
            style.get("main_category", _STYLE_CATEGORY_FALLBACK).casefold(),
            _style_label_sort_key(style.get("raw_label") or style["label"]),
        )
    )
    return categorized


